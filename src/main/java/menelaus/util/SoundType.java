package menelaus.util;

/**
 * Each sound type for game.
 * @author Obatola Seward-Evans
 *
 */
public enum SoundType {
	/** identify sound for button clicks. */
	BUTTONSOUND,
	
	/** identify sound exiting the game. */
	EXITSOUND, 
	
	/** identify sound for winning a game. */
	WINSOUND, 
	
	/** identify sound for losing a game. */
	LOSESOUND, 
	
	/** identify sound for completing something. */
	COMPLETESOUND,
	
	/** identify sound for closing the app. */
	CLOSEAPP, 
	
	/** identify sound for pressing a piece. */
	PRESSPIECE, 
	
	/** identify sound for pressing a tile. */
	PRESSTILE,
	
	/** identify sound for restarting a game. */
	RESTARTSOUND
}
